#include <iostream>

void run_mitm_message(std::ostream& log) {
    log << "Задание 5. Сообщение на тему: Атака посередине\n\n";
    log << "Атака посередине — это ситуация, когда злоумышленник находится между двумя участниками обмена "
           "и незаметно перехватывает или подменяет их сообщения. Например, Алиса хочет передать данные Бобу "
           "через криптографический протокол, а Ева вклинивается в канал связи.\n\n";
    log << "В протоколе Хьюза участники используют открытые параметры и формируют общий секретный ключ. "
           "Проблема возникает тогда, когда открытые значения никак не подтверждаются. Ева может отправить Алисе "
           "свои параметры вместо параметров Боба, а Бобу — свои параметры вместо параметров Алисы. В результате "
           "Алиса думает, что работает с Бобом, Боб думает, что работает с Алисой, но фактически оба обмениваются "
           "данными через Еву.\n\n";
    log << "После такой подмены Ева получает отдельный ключ с каждой стороной. Она может расшифровать сообщение от одного "
           "участника, прочитать его, затем заново зашифровать и отправить дальше. При этом для Алисы и Боба обмен "
           "может выглядеть обычным и корректным.\n\n";
    log << "Основная причина уязвимости — отсутствие аутентификации. Само шифрование защищает данные от простого "
           "подслушивания, но не гарантирует, что на другой стороне действительно находится нужный пользователь. "
           "Для защиты от атаки посередине применяют цифровые подписи, сертификаты и проверку подлинности открытых ключей.\n";
}

